//Basic Operations
add = (x, y) => {
    sum = parseFloat(x) + parseFloat(y);
    return sum;
}

subtract = (x, y) => {
    difference = parseFloat(x) - parseFloat(y);
    return difference;
}

multiply = (x, y) => {
    product = parseFloat(x) * parseFloat(y);
    return product;
}

divide = (x, y) => {
    quotient = parseFloat(x) / parseFloat(y);;
    return quotient;
}
